package org.oa.ajax_rest_demo.model;

public class Food {
	Integer idFood;
	String name;
	Float price;
	
	public Integer getIdFood() {
		return idFood;
	}
	public void setIdFood(Integer id) {
		this.idFood = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Float getPrice() {
		return price;
	}
	public void setPrice(Float price) {
		this.price = price;
	}	
}
